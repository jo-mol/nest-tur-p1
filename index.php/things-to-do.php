			    <li>Salim Ali Bird Sanctuary is also known as Thekkady Bird Sanctuary and located 73 km from Munnar. It houses plenty of local and migratory birds – crimson-fronted barbet, crested serpent eagle, peninsular bay owl, fairy bluebird and rose billed roller to name a few. Malabar grey hornbill, Sri Lanka frogmouth, and Indian hornbill and Nilgiri wood pigeon can also be spotted.</li>
