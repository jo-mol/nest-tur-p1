				 <h3>3. Go Camping and Trekking in Munnar</h3>
				 
				 <img src="../images/trekking.jpg"/>
				 
				 <p style="font-family:Palatino Linotype;color:black;font-size:17px;">Munnar is bliss for journey enthusiasts and fitness freaks, especially trekkers. Trekking till high elevations like Echo point and pinnacle Station is one of the maximum favored activities in Munnar. Located 15 km from Munnar, Echo point is a spellbinding vacation spot wherein voice resonance takes place certainly. Pinnacle Station is the highest point in Munnar and offers a stunning view of the town surrounded with the aid of hills and valleys. Hiking 32 km up to top Station is pretty exhaustive and calls for a lot of stamina. If you will accomplish the goal, the workout quota for the whole week is taken care of. Don't forget to carry your walking footwear and trekking gears.</p>
				 
				 <h3>4. Spot rare animal and bird species at the famous Wildlife Sanctuaries</h3>
				 
				  <img src="../images/animal.jpg"/>
				  
				  
				 <p style="font-family:Palatino Linotype;color:black;font-size:17px;">daring hearts! let’s discover the thick and darkish forests, biosphere reserves, countrywide park and sanctuaries of Munnar. Who is aware of, you can be lucky to spot a few endangered and almost extinct animals and birds.  The most visited and popular wildlife areas of Munnar are Eravikulam National Park, Chinnar natural world Sanctuary and Salim Ali Bird Sanctuary.</p>
				 
				 <ul>
				 <li>Eravikulam National Park, is located in the Kannan Devan hills of Munnar. The national park is famous for endangered Nilgiri Tahr. The forest spread over an area of 97 sq km is divided into; core area, buffer area and tourism area.</li>
